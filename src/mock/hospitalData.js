// 模擬 API 服務
export const mockApi = {
    // 查詢醫院列表
    searchHospitals: (params) => {
        return new Promise((resolve) => {
            setTimeout(() => {
                let result = [...hospitals];

                // 依條件過濾
                if (params.hospitalId) {
                    result = result.filter(h => h.id.includes(params.hospitalId));
                }
                if (params.hospitalName) {
                    result = result.filter(h => h.name.includes(params.hospitalName));
                }
                if (params.hospitalType) {
                    const typeMap = {
                        "01": "醫學中心",
                        "02": "區域醫院",
                        "03": "地區醫院",
                        "04": "診所"
                    };
                    result = result.filter(h => h.typeName === typeMap[params.hospitalType]);
                }

                resolve(result);
            }, 300); // 模擬網路延遲
        });
    },

    // 新增醫院
    createHospital: (data) => {
        return new Promise((resolve) => {
            setTimeout(() => {
                const newHospital = {
                    id: data.hospitalId,
                    name: data.hospitalName,
                    typeName: {
                        "01": "醫學中心",
                        "02": "區域醫院",
                        "03": "地區醫院",
                        "04": "診所"
                    }[data.hospitalType],
                    address: data.address,
                    phone: data.phone,
                    ownerName: data.ownerName,
                    status: "特約中"
                };
                hospitals.push(newHospital);
                resolve(newHospital);
            }, 300);
        });
    },

    // 更新醫院
    updateHospital: (id, data) => {
        return new Promise((resolve, reject) => {
            setTimeout(() => {
                const index = hospitals.findIndex(h => h.id === id);
                if (index === -1) {
                    reject(new Error('找不到該醫院'));
                    return;
                }

                hospitals[index] = {
                    ...hospitals[index],
                    name: data.hospitalName,
                    typeName: {
                        "01": "醫學中心",
                        "02": "區域醫院",
                        "03": "地區醫院",
                        "04": "診所"
                    }[data.hospitalType],
                    address: data.address,
                    phone: data.phone,
                    ownerName: data.ownerName
                };

                resolve(hospitals[index]);
            }, 300);
        });
    },

    // 刪除醫院
    deleteHospital: (id) => {
        return new Promise((resolve, reject) => {
            setTimeout(() => {
                const index = hospitals.findIndex(h => h.id === id);
                if (index === -1) {
                    reject(new Error('找不到該醫院'));
                    return;
                }

                hospitals[index].status = "終止特約";
                resolve(true);
            }, 300);
        });
    }
};